import { I18n } from '@mantou/gem/helper/i18n';

import type { SupportLang } from '../../common/config';
import { isGitLab } from '../lib/utils';

export function getPlatform() {
  return isGitLab() ? 'Gitlab' : 'Github';
}

const resources = {
  en: {
    editOnGithub: 'Edit this page on $1',
    createOnGithub: 'Create on $1',
    footer: 'Generated by $1<GemBook>',
    lastUpdated: 'Last Updated',
  },
  zh: {
    editOnGithub: '在 $1 编辑此页',
    createOnGithub: '在 $1 上创建此页面',
    footer: '通过 $1<GemBook> 生成',
    lastUpdated: '上次更新',
  },
} satisfies Record<SupportLang, unknown>;

export const originDocLang = document.documentElement.lang;

export const selfI18n = new I18n<typeof resources.en>({
  fallbackLanguage: originDocLang in resources ? originDocLang : 'en',
  resources,
  // 没有启动 i18n 时由 `html[lang]` 定义语言
  currentLanguage: originDocLang,
});
